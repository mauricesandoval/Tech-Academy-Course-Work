#!/usr/bin/python3

# Assigning Values

# The assignment opertor is used for assigning a value in Python and
# it will also create an object if that has not already been created

def main():
    a = 1
    print(a)

if __name__ == "__main__": main()


# 'one' spelled out
def main():
    a = "one"
    print(a)

if __name__ == "__main__": main()


# use type function to print the type and value - string
def main():
    a = "one"
    print(type(a),a) #use type() function. Type of thing is usually 
                     #the name of the class
if __name__ == "__main__": main()



# use type function to print the type and value - integer
def main():
    a = 7
    print(type(a),a)
    
if __name__ == "__main__": main()


# use with two values
def main():
    a, b = 0,1
    print(a,b)
    
if __name__ == "__main__": main()

#
def main():
    a, b = 0, 1
    a, b = b, a  # swaps values
    print(a, b)
    
if __name__ == "__main__": main()


# Tuples
def main():
    a = (1,2,3,4,5) # Parenthesis
    print(a)
    
if __name__ == "__main__": main()


# List
def main():
    a = [1,2,3,4,5]  # square brackets
    print(a)
    
if __name__ == "__main__": main()
















